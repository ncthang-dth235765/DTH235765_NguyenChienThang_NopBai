#Câu 4: Python hỗ trợ những kiểu dữ liệu cơ bản nào?
'''
- Kiểu số (Numbers):
    int (số nguyên): Ví dụ 5, -3, 100
    float (số thực): Ví dụ 3.14, -0.001, 2.0
    complex (số phức): Ví dụ 1+2j, 3-4j
- Kiểu chuỗi (String):
    str: Chuỗi ký tự, ví dụ "Hello", 'Python'
- Kiểu Boolean:
    bool: Chỉ có hai giá trị True và False
- Kiểu danh sách (List):
    list: Một tập hợp các phần tử có thể khác kiểu nhau, ví dụ [1, 2, 'a', True]
- Kiểu tuple:
    tuple: Giống list nhưng không thay đổi được (immutable), ví dụ (1, 2, 3)
- Kiểu tập hợp (Set):
    set: Tập hợp các phần tử duy nhất, ví dụ {1, 2, 3}
- Kiểu từ điển (Dictionary):
    dict: Bộ sưu tập các cặp key-value, ví dụ {'name': 'Alice', 'age': 25}
    
'''