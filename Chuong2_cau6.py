#Câu 6: Trình bày ý nghĩa toán tử /, //, %, **, and, or, is.
'''
- / : Phép chia thực (chia ra số thập phân).
Ví dụ: 7 / 2 = 3.5
- // : Phép chia lấy phần nguyên (chia lấy nguyên, bỏ phần thập phân).
Ví dụ: 7 // 2 = 3
- % : Phép chia lấy phần dư (modulus).
Ví dụ: 7 % 2 = 1
- ** : Phép lũy thừa.
Ví dụ: 3 ** 2 = 9
- and : Toán tử logic "và", trả về True nếu cả hai biểu thức đều đúng.
Ví dụ: True and False = False
- or : Toán tử logic "hoặc", trả về True nếu ít nhất một biểu thức đúng.
Ví dụ: True or False = True
- is : Toán tử so sánh, kiểm tra hai biến có cùng tham chiếu (cùng đối tượng) hay không.
Ví dụ:
a = [1, 2]
b = a
a is b

'''